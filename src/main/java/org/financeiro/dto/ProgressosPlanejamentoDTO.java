package org.financeiro.dto;

public class ProgressosPlanejamentoDTO {

	private Double mensal;
	private Double anual;
	private Double todo;

	public Double getMensal() {
		return mensal;
	}
	public void setMensal(Double mensal) {
		this.mensal = mensal;
	}
	public Double getAnual() {
		return anual;
	}
	public void setAnual(Double anual) {
		this.anual = anual;
	}
	public Double getTodo() {
		return todo;
	}
	public void setTodo(Double todo) {
		this.todo = todo;
	}
}
